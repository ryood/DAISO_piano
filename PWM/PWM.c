volatile uint16_t phaseAccumlator;
volatile uint16_t tuningWord;
volatile uint8_t amp;

//=============================================================================
// 波形生成
//
// ----------------------------------------------------------------------------
// setDDSParameter()
// parameter: frequency: 生成する周波数
//
void setDDSParameter(float frequency)
{
	tuningWord = (int16_t)(frequency * POW_2_16 / SAMPLE_CLOCK);
}

// ----------------------------------------------------------------------------
// generateWave()
// return: 出力値(0..255)
//
uint8_t generateSawWave()
{
	uint8_t index;
	
	phaseAccumlator += tuningWord;
	
	// 右へシフト: 16bit -> 6bit(64個)
	index = phaseAccumlator >> 10;
	return pgm_read_byte(&sineTable[index]);
}

//=============================================================================
// 波形生成
//
// ----------------------------------------------------------------------------
// setPWMDuty()
// parameter: value: 設定するDuty(0 .. OCR0A)
//
void setPWMDuty(uint8_t value)
{
	// PWMのデューティー比を設定
	OCR0B = value;
}

//=============================================================================
// ADC
//
// ----------------------------------------------------------------------------
// getCV()
// return: CV値(0..4095)
//
uint16_t getCV() {
	// ADC_CVの値を取得
	return 901;    // 440Hz
	//return 4095;    // 2000Hz
}

// ----------------------------------------------------------------------------
// getGate()
// return: Gate値(0..255)
//
uint8_t getGate() {
	// ADC_GATEの値を取得
	return 255;
}

//=============================================================================
// ラッチ割込み
//
//ISR()
//    generateSawWave()を呼び出し
//    amp値を乗算
//    PWM DACに出力値を設定

//=============================================================================
// メイン・ルーチン
//
int main()
{
	uint16_t cv;
	uint8_t v;
	
	//-------------------------------------------------------------------------
	// PORT設定
	//-------------------------------------------------------------------------
	// DDRD = 0;
	DDRD |= (1 << DDD5);    // PD5(OC0B): PWM out
	// Debug用
	//
	DDRD |= (1 << DDB6);    // PD6: output
	
	//-------------------------------------------------------------------------
	// PWM設定
	//-------------------------------------------------------------------------
	// TCCR0A = 0;
	// TCCR0B = 0;
	//-------------------------------------------------------------------------
	// 波形生成モード: WGM0: 1:1:1
	// 高速PWM(モード7)
	TCCR0A |= (1 << WGM01) | (1 << WGM00);
	TCCR0B |= (1 << WGM02);
	//-------------------------------------------------------------------------
	// コンペア・アウトプットB: COM0B: 1:0
	// コンペア・マッチでOC0Bクリア、TOPでOC0Bセット
	TCCR0A |= (1 << COM0B1) | (0 << COM0B0);
	//-------------------------------------------------------------------------
	// クロック設定: CS0: 0:0:1
	// 分周なし
	TCCR0B |= (0 << CS02) | (0 << CS01) | (1 << CS00);
	
	// TCCR0A = 0b00100011;
	// TCCR0B = 0B00001001;
	
	//-------------------------------------------------------------------------
	// 分解能 6bit(0 .. 63)
	OCR0A = 64;
	
	for (;;) {
		// Debug用: PD6
		PORTD |= (1 << PORTD6);
		
		cv = getCV();
		setDDSParameter(cv * FREQUENCY_MAX / 4096.0f);
		amp = getGate();
		v = generateSawWave();
		setPWMDuty(v);
		
		// Debug用: PD6
		PORTD &= ~(1 << PORTD6);
		
		_delay_us(100);    // 8,000Hz
	}
}
